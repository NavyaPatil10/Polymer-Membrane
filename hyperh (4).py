def plot_rec_curve(y_true, y_pred, label, color):
    errors = np.abs(y_true - y_pred)
    error_thresholds = np.linspace(0, max(errors), 100)

    accuracy = [(errors <= t).mean() for t in error_thresholds]

    plt.plot(
        error_thresholds,
        accuracy,
        label=label,
        linewidth=2,      # ✅ curve width = 2
        color=color
    )

