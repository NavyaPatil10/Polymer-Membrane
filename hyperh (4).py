# ------------------------------------------------------------
# 1️⃣  Correct Pareto-efficient function
# ------------------------------------------------------------
def is_pareto_efficient(points):
    """
    Returns boolean mask of Pareto-efficient points.
    Assumes larger values are better for all objectives.
    """
    n_points = points.shape[0]
    is_efficient = np.ones(n_points, dtype=bool)

    for i in range(n_points):
        if not is_efficient[i]:
            continue

        # A point j dominates i if:
        # j >= i in ALL objectives
        # AND j > i in at least one objective
        dominates = np.all(points >= points[i], axis=1) & \
                    np.any(points > points[i], axis=1)

        is_efficient[dominates] = False
        is_efficient[i] = True

    return is_efficient


# ------------------------------------------------------------
# 2️⃣  Prepare optimization matrix
#     (HIGH perm, HIGH selectivity, LOW uncertainty)
# ------------------------------------------------------------

